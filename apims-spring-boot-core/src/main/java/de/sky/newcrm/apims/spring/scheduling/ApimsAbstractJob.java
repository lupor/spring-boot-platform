package de.sky.newcrm.apims.spring.scheduling;

public abstract class ApimsAbstractJob implements ApimsJob {

    private boolean enabled;

    protected ApimsAbstractJob(boolean enabled) {
        this.enabled = enabled;
    }

    @Override
    public boolean isEnabled() {
        return enabled;
    }

    @Override
    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }
}
